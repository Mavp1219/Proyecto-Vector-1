    private void cmdcalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdcalcularActionPerformed

        if (txtlongitud.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite la longitud del vector", "Error", JOptionPane.ERROR_MESSAGE);
            txtlongitud.requestFocusInWindow();
        } else if (Integer.parseInt(txtlongitud.getText().trim()) == 0) {
            JOptionPane.showMessageDialog(this, "La longitud debe ser mayor que cero", "Error", JOptionPane.ERROR_MESSAGE);
            txtlongitud.requestFocusInWindow();
            txtlongitud.selectAll();
        } else {

            int longitud, opc;
            double sumatoria = 0, productoria = 1, mayor = 0, menor = 0;
            longitud = Integer.parseInt(txtlongitud.getText());
            opc = cmboperaciones.getSelectedIndex();

            switch (opc) {
                case 0:
                    for (int i = 0; i < v.length; i++) {
                        sumatoria = v[i] + sumatoria;
                    }
                    txtresultado.setText(sumatoria + "");
                    break;

                case 1:
                    for (int i = 0; i < v.length; i++) {
                        productoria = v[i] * productoria;
                    }
                    txtresultado.setText(productoria + "");
                    break;

                case 2:
                    for (int i = 0; i < v.length; i++) {
                        if (i == 0) {
                            mayor = v[0];
                            menor = v[0];
                        }

                        if (v[i] > mayor) {
                            mayor = v[i];
                        }
                    }
                    txtresultado.setText(mayor + "");
                    break;

                case 3:
                    for (int i = 0; i < v.length; i++) {
                        if (i == 0) {
                            mayor = v[0];
                            menor = v[0];
                        }

                        if (v[i] < menor) {
                            menor = v[i];
                        }
                    }
                    txtresultado.setText(menor + "");
                    break;
            }

        }
    }//GEN-LAST:event_cmdcalcularActionPerformed

